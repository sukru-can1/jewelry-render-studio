def main() -> None:
    parsed = args()
    load_env(Path(".env"))
    api_key = os.environ.get("RUNPOD_API_KEY")
    endpoint_id = parsed.endpoint_id or os.environ.get("RUNPOD_ENDPOINT_ID", "4lvi3w848rqy0l")
    if not api_key:
        raise SystemExit("RUNPOD_API_KEY is missing.")

    base = json.loads(Path(parsed.base_recipe).read_text(encoding="utf-8-sig"))
    recipe_dir = Path(parsed.output_root) / "recipes"
    recipe_dir.mkdir(parents=True, exist_ok=True)
    previews = make_preview_candidates(base, parsed.prefix, parsed.preview_resolution, parsed.preview_samples)
    preview_paths = []
    for recipe in previews:
        path = recipe_dir / f"{recipe['name']}.json"
        path.write_text(json.dumps(recipe, indent=2), encoding="utf-8")
        preview_paths.append(path)

    jobs = submit_jobs(previews, parsed, api_key, endpoint_id, "preview")
    results = wait_and_download(jobs, parsed, api_key, endpoint_id, parsed.timeout_seconds)
    for result in results:
        projection_path = Path(parsed.output_root) / f"{result['job']['name']}.projection.png"
        result["projection_path"] = projected_final_look(result["image_path"], projection_path, parsed.projection_resolution)
        result["preview_score"] = score_preview(result["image_path"], result["metadata"], result["job"]["recipe"])
        result["score"] = score_preview(result["projection_path"], result["metadata"], result["job"]["recipe"])
        print(
            json.dumps(
                {
                    "preview": result["job"]["name"],
                    "preview_score": result["preview_score"],
                    "projection_score": result["score"],
                    "projection_path": str(result["projection_path"]),
                },
                indent=2,
            )
        )

    results.sort(key=lambda item: item["score"]["score"], reverse=True)
    contact_sheet = Path(parsed.output_root) / f"{parsed.prefix}_preview_contact_sheet.jpg"
    make_contact_sheet(results, contact_sheet, 380)
    projection_contact_sheet = Path(parsed.output_root) / f"{parsed.prefix}_projection_contact_sheet.jpg"
    make_contact_sheet(results, projection_contact_sheet, 380, image_key="projection_path")
    best = results[0]
    print(
        json.dumps(
            {
                "best_projection": best["job"]["name"],
                "score": best["score"],
                "preview_contact_sheet": str(contact_sheet),
                "projection_contact_sheet": str(projection_contact_sheet),
            },
            indent=2,
        )
    )

    final_recipe = copy.deepcopy(best["job"]["recipe"])
    final_recipe["name"] = f"{parsed.prefix}_final_from_{best['job']['name']}"
    final_recipe["description"] = f"Final high-quality render selected by preview_refine_render.py from preview {best['job']['name']}."
    final_recipe["render"]["resolution"] = [parsed.final_resolution, parsed.final_resolution]
    final_recipe["render"]["samples"] = parsed.final_samples
    final_path = recipe_dir / f"{final_recipe['name']}.json"
    final_path.write_text(json.dumps(final_recipe, indent=2), encoding="utf-8")
    print(json.dumps({"final_recipe": str(final_path)}))

    if parsed.render_final:
        final_jobs = submit_jobs([final_recipe], parsed, api_key, endpoint_id, "final")
        final_results = wait_and_download(final_jobs, parsed, api_key, endpoint_id, parsed.timeout_seconds)
        print(json.dumps({"final_image_url": final_results[0]["image_url"], "final_image_path": str(final_results[0]["image_path"])}, indent=2))


if __name__ == "__main__":
    main()
