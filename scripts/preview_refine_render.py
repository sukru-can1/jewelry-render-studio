def load_env(path: Path) -> None:
    if not path.exists():
        return
    for raw_line in path.read_text(encoding="utf-8-sig").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        key, value = line.split("=", 1)
        os.environ.setdefault(key, value.strip().strip('"'))


def request_json(url: str, api_key: str, payload: dict | None = None) -> dict:
    data = json.dumps(payload).encode("utf-8") if payload is not None else None
    request = urllib.request.Request(
        url,
        data=data,
        method="POST" if payload is not None else "GET",
        headers={"Authorization": f"Bearer {api_key}", "Content-Type": "application/json"},
    )
    with urllib.request.urlopen(request, timeout=120) as response:
        return json.loads(response.read().decode("utf-8"))


def download(url: str, output: Path) -> None:
    output.parent.mkdir(parents=True, exist_ok=True)
    with urllib.request.urlopen(url, timeout=120) as response:
        output.write_bytes(response.read())


def adjust_light(recipe: dict, token: str, scale: float) -> None:
    for item in recipe.get("source_scene", {}).get("light_adjustments", []):
        if token in item.get("contains", []):
            item["power_scale"] = scale


def adjust_card(recipe: dict, token: str, value: float, strength: float) -> None:
    for item in recipe.get("source_scene", {}).get("object_adjustments", []):
        if token not in item.get("contains", []):
            continue
        adjust = item.setdefault("source_material_adjust", {})
        color = [value, value, value + 0.003, 1.0]
        adjust["base_color"] = color
        adjust["diffuse_color"] = color
        adjust["emission_color"] = color
        adjust["emission_strength_scale"] = strength


