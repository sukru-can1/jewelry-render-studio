def object_bounds(metadata: dict, image_size: tuple[int, int], tokens: list[str], fallback: list[float]) -> tuple[int, int, int, int]:
    lowered = [token.lower() for token in tokens]
    metadata_size = None
    recipe_resolution = metadata.get("recipe", {}).get("render", {}).get("resolution") if isinstance(metadata, dict) else None
    if isinstance(recipe_resolution, list) and len(recipe_resolution) == 2:
        try:
            metadata_size = (max(1, int(recipe_resolution[0])), max(1, int(recipe_resolution[1])))
        except (TypeError, ValueError):
            metadata_size = None
    for item in metadata.get("object_image_bounds", []):
        signature = str(item.get("signature") or item.get("name") or "").lower()
        bounds = item.get("bounds_px")
        if any(token in signature for token in lowered) and isinstance(bounds, list) and len(bounds) == 4:
            left, top, right, bottom = [int(value) for value in bounds]
            if metadata_size and metadata_size != image_size:
                sx = image_size[0] / metadata_size[0]
                sy = image_size[1] / metadata_size[1]
                return (int(left * sx), int(top * sy), int(right * sx), int(bottom * sy))
            return (left, top, right, bottom)
    width, height = image_size
    left, top, right, bottom = fallback
    return (int(left * width), int(top * height), int(right * width), int(bottom * height))


def ellipse_half_mask(size: tuple[int, int], side: str) -> Image.Image:
    width, height = size
    ellipse = Image.new("L", size, 0)
    draw = ImageDraw.Draw(ellipse)
    pad_x = int(width * 0.04)
    pad_y = int(height * 0.04)
    draw.ellipse((pad_x, pad_y, width - pad_x, height - pad_y), fill=255)
    half = Image.new("L", size, 0)
    draw = ImageDraw.Draw(half)
    if side == "left":
        draw.rectangle((0, 0, width // 2 + int(width * 0.08), height), fill=255)
    else:
        draw.rectangle((width // 2 - int(width * 0.08), 0, width, height), fill=255)
    return ImageChops.multiply(ellipse, half).filter(ImageFilter.GaussianBlur(4))


def masked_stats(image: Image.Image, mask: Image.Image) -> dict:
    gray = image.convert("L")
    edges = gray.filter(ImageFilter.FIND_EDGES)
    gray_values = list(gray.getdata())
    edge_values = list(edges.getdata())
    mask_values = list(mask.getdata())
    values = [value for value, alpha in zip(gray_values, mask_values) if alpha > 24]
    edge_sample = [value for value, alpha in zip(edge_values, mask_values) if alpha > 24]
    if not values:
        return {"mean": 0.0, "std": 0.0, "edge": 0.0, "clarity": 0.0}
    mean = sum(values) / len(values)
    variance = sum((value - mean) ** 2 for value in values) / len(values)
    std = variance ** 0.5
    edge = sum(edge_sample) / max(1, len(edge_sample))
    clarity = std + edge * 0.35 - max(0.0, mean - 154.0) * 0.12
    return {"mean": mean, "std": std, "edge": edge, "clarity": clarity}


def score_preview(image_path: Path, metadata: dict, recipe: dict) -> dict:
    with Image.open(image_path) as source:
        image = source.convert("RGB")
    bounds = object_bounds(
        metadata,
        image.size,
        ["diamond_round_11"],
        recipe.get("postprocess", {}).get("center_stone", {}).get("fallback_bounds_norm", [0.33, 0.22, 0.67, 0.58]),
    )
    left, top, right, bottom = bounds
    pad = max(6, int((right - left) * 0.12))
    left = max(0, left - pad)
    top = max(0, top - pad)
    right = min(image.width, right + pad)
    bottom = min(image.height, bottom + pad)
    crop = image.crop((left, top, right, bottom))
    left_stats = masked_stats(crop, ellipse_half_mask(crop.size, "left"))
    right_stats = masked_stats(crop, ellipse_half_mask(crop.size, "right"))
    symmetry = abs(left_stats["clarity"] - right_stats["clarity"])
    brightness_penalty = abs(((left_stats["mean"] + right_stats["mean"]) * 0.5) - 176.0) * 0.12
    clarity = (left_stats["clarity"] + right_stats["clarity"]) * 0.5
    score = clarity - symmetry * 2.2 - brightness_penalty
    return {
        "score": score,
        "symmetry_delta": symmetry,
        "left": left_stats,
        "right": right_stats,
        "brightness_penalty": brightness_penalty,
    }


def projected_final_look(image_path: Path, output_path: Path, resolution: int) -> Path:
    with Image.open(image_path) as source:
        image = source.convert("RGB")

    image = ImageOps.contain(image, (resolution, resolution), Image.Resampling.LANCZOS)
    canvas = Image.new("RGB", (resolution, resolution), "white")
    canvas.paste(image, ((resolution - image.width) // 2, (resolution - image.height) // 2))

    # Preview renders are noisier and flatter than final renders. This projection applies
    # the expected final-grade direction so selection is based on the likely final look.
    projected = ImageEnhance.Contrast(canvas).enhance(1.08)
    projected = ImageEnhance.Brightness(projected).enhance(0.992)
    projected = ImageEnhance.Color(projected).enhance(0.96)
    projected = ImageEnhance.Sharpness(projected).enhance(1.10)
    projected = projected.filter(ImageFilter.UnsharpMask(radius=0.8, percent=70, threshold=2))

    output_path.parent.mkdir(parents=True, exist_ok=True)
    projected.save(output_path, quality=96)
    return output_path


def submit_jobs(recipes: list[dict], parsed: argparse.Namespace, api_key: str, endpoint_id: str, phase: str) -> list[dict]:
    jobs = []
    for recipe in recipes:
        name = recipe["name"]
        output_prefix = f"{parsed.output_root.rstrip('/')}/{name}"
        payload = {
            "input": {
                "job_id": name,
                "model": {"url": parsed.model_url, "pathname": parsed.model_pathname},
                "recipe": recipe,
                "output": {"prefix": output_prefix},
            }
        }
        submitted = request_json(f"https://api.runpod.ai/v2/{endpoint_id}/run", api_key, payload)
        runpod_job_id = submitted.get("id") or submitted.get("jobId")
        if not runpod_job_id:
            raise SystemExit(json.dumps(submitted, indent=2))
        jobs.append({"name": name, "runpod_job_id": runpod_job_id, "output_prefix": output_prefix, "recipe": recipe})
        print(json.dumps({"phase": phase, "submitted": name, "runpod_job_id": runpod_job_id}))
    return jobs


def wait_and_download(jobs: list[dict], parsed: argparse.Namespace, api_key: str, endpoint_id: str, timeout_seconds: int) -> list[dict]:
    pending = {job["name"]: job for job in jobs}
    completed: dict[str, dict] = {}
    deadline = time.time() + timeout_seconds
    while pending and time.time() < deadline:
        time.sleep(8)
        for name, job in list(pending.items()):
            status = request_json(f"https://api.runpod.ai/v2/{endpoint_id}/status/{job['runpod_job_id']}", api_key)
            state = status.get("status")
            print(json.dumps({"name": name, "status": state}))
            if state == "COMPLETED":
                completed[name] = status.get("output", {})
                del pending[name]
            elif state in {"FAILED", "CANCELLED", "TIMED_OUT"}:
                raise SystemExit(json.dumps({"name": name, "status": status}, indent=2))

    if pending:
        raise SystemExit(json.dumps({"error": "Timed out waiting for jobs.", "pending": sorted(pending)}, indent=2))

    results = []
    for job in jobs:
        name = job["name"]
        output = completed[name]
        image_url = output.get("image_url") or f"{PUBLIC_BASE_URL}/{job['output_prefix']}/{name}.png"
        metadata_url = output.get("metadata_blob", {}).get("url") or f"{PUBLIC_BASE_URL}/{job['output_prefix']}/{name}.json"
        image_path = Path(parsed.output_root) / f"{name}.png"
        metadata_path = Path(parsed.output_root) / f"{name}.metadata.json"
        download(image_url, image_path)
        try:
            download(metadata_url, metadata_path)
            metadata = json.loads(metadata_path.read_text(encoding="utf-8-sig"))
        except Exception:
            metadata = {}
        results.append({"job": job, "image_url": image_url, "metadata_url": metadata_url, "image_path": image_path, "metadata": metadata})
        print(json.dumps({"name": name, "image_url": image_url, "metadata_url": metadata_url}))
    return results


def make_contact_sheet(results: list[dict], output: Path, tile_size: int, image_key: str = "image_path") -> None:
    cols = min(3, max(1, len(results)))
    rows = math.ceil(len(results) / cols)
    label_h = 42
    sheet = Image.new("RGB", (cols * tile_size, rows * (tile_size + label_h)), "white")
    draw = ImageDraw.Draw(sheet)
    for index, result in enumerate(results):
        col = index % cols
        row = index // cols
        x = col * tile_size
        y = row * (tile_size + label_h)
        with Image.open(result[image_key]) as img:
            tile = img.convert("RGB")
            tile.thumbnail((tile_size, tile_size))
            sheet.paste(tile, (x + (tile_size - tile.width) // 2, y + label_h + (tile_size - tile.height) // 2))
        score = result.get("score", {})
        label = f"{result['job']['name']} score={score.get('score', 0):.1f} sym={score.get('symmetry_delta', 0):.1f}"
        draw.text((x + 8, y + 6), label[:70], fill=(0, 0, 0))
    output.parent.mkdir(parents=True, exist_ok=True)
    sheet.save(output)


